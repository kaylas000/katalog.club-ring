export function searchLocations(query: string): Location[] {
  if (query.length < 2) return [];
  const q = query.toLowerCase();
  return locations
    .filter((loc) => {
      return (
        loc.label.toLowerCase().includes(q) ||
        (loc.region && loc.region.toLowerCase().includes(q)) ||
        (loc.parentLabel && loc.parentLabel.toLowerCase().includes(q))
      );
    })
    .sort((a, b) => {
      const aExact = a.label.toLowerCase() === q;
      const bExact = b.label.toLowerCase() === q;
      if (aExact && !bExact) return -1;
      if (!aExact && bExact) return 1;
      const aStarts = a.label.toLowerCase().startsWith(q);
      const bStarts = b.label.toLowerCase().startsWith(q);
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;
      const kindOrder: Record<LocationKind, number> = {
        city: 0,
        settlement: 1,
        district: 2,
        region: 3,
      };
      return kindOrder[a.kind] - kindOrder[b.kind];
    })
    .slice(0, 10);
}

export function getLocationById(id: number): Location | undefined {
  return locations.find((l) => l.id === id);
}
